          $rowIndex[] = $i;
          $colIndex[] = $column;
          $rowString[] = $matrix[$i][0];
          $colString[] = $matrix[0][$column];
        };
      };
      do {
        $randomKey = array_rand($rowIndex);
        $getIndexRowRand = $rowIndex[$randomKey];
        $getIndexColRand = $colIndex[$randomKey]; 
        $shuffleNewIndexRow = (($getIndexRowRand * 25) + 11) % count($matrix);
