    $rowIndex = [];
    $colIndex = [];
    $rowString = [];
    $colString = [];
    $matrix = self::detect_language($word);
    $getIndexRowRand;
    $getIndexColRand;
    $text = mb_str_split($word, 1, 'UTF-8');
    $result = "";
