  private static function detect_language($word) {
    $text = trim($word);
    if (preg_match('/[\x{0600}-\x{06FF}]/u', $text)) {
      return self::arabic();
    } elseif (preg_match('/^[a-zA-Z\s[:punct:]]+$/', $text)) {
      return self::english();
    };
  }
