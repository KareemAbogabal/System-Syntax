const avatars = document.querySelectorAll(".contact .avatar[data-name]");
const onlineUsers = document.querySelectorAll(".contact.is-online");
const onlineCount = document.getElementById("onlineCount");
const searchInput = document.getElementById("searchInput");
const usersList = document.getElementById("usersList");

function getInitials(name) {
  if (!name) return "?";
  const parts = name.trim().split(/\s+/).filter(Boolean);
  if (parts.length === 1) {
    return parts[0].slice(0, 2).toUpperCase();
  }
  return (parts[0][0] + parts[1][0]).toUpperCase();
};

avatars.forEach(avatar => {
  const name = avatar.dataset.name || "";
  const image = avatar.dataset.image || "";
  if (image) {
    avatar.innerHTML = `<img src="${image}" alt="${name}">`;
  } else {
    avatar.textContent = getInitials(name);
  }
});

onlineCount.textContent = onlineUsers.length;

searchInput.addEventListener("input", () => {
  const q = searchInput.value.trim().toLowerCase();
  const cards = usersList.querySelectorAll(".contact");
  cards.forEach(card => {
    const name = card.querySelector(".top h3")?.textContent.toLowerCase() || "";
    const message = card.querySelector(".message")?.textContent.toLowerCase() || "";
    const show = name.includes(q) || message.includes(q);
    card.style.display = show ? "" : "none";
  });
});

